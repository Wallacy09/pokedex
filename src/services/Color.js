export const getColors = (type) => {
    switch (type) {

        case 'fairy':
            return '#E9CDEA';

        case 'bug':
            return '#76A866';

        case 'flying':
            return '#A4B8C9';

        case 'dark':
            return '#A79FA6';

        case 'normal':
            return '#BF9762';

        case 'dragon':
            return '#004170';

        case 'grass':
            return '#729F92';

        case 'electric':
            return '#F2E7AA';

        case 'poison':
            return '#C5A9C4';

        case 'fighting':
            return '#D88EAB';

        case 'fire':
            return '#EAAB7D';

        case 'ice':
            return '#B5DCD5';

        case 'ghost':
            return '#9B9FB9';

        case 'water':
            return '#71C3FF';

        case 'rock':
            return '#D2CAB3';

        case 'ground':
            return '#E1A48A';

        case 'steel':
            return '#C8C8C8';

        case 'psychic':
            return '#F3A9A9';
    }
};